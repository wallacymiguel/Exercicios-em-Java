        // TODO add your handling code here:
        int a = Integer.parseInt(txtAno.getText());
        int i = 2018 - a;
        if (i<16){
            lblResultado.setText("Não vota");
        } else {
            if (i >= 16 && i < 18 || i > 70){
                lblResultado.setText("Opcional");
            } else{
                lblResultado.setText("Obrigatório");
            }
        }
        lblIdade.setVisible(true);
        lblIdade.setText("Sua idade é " + i);
